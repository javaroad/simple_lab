package base;

public class SwitchTest {
	public static void main(String[] args) {
		switch (4) {
		case 6:
			System.out.println("6");
		case 5:
			System.out.println("5");
		case 4:
			System.out.println("4");
		case 3:
			System.out.println("3");
		case 2:
			System.out.println("2");
		case 1:
			System.out.println("1");
		default:
			break;
		}
	}
}